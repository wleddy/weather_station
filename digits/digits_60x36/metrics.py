"""
A class to descive the dimensions and maybe some
other info about the digit image files in this directory
"""

class metrics:
    
    # most of the characters will be WIDTH wide
    # some narrower chars are listed here.
    self.WIDTH = 36
    self.HEIGHT = 60
    self.DOT_WIDTH = 24
    self.COLON_WIDTH = 24
    self.DASH_WIDTH = 30
    self.ONE_WIDTH = 30